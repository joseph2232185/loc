//Definición: Spring Boot gestiona las dependencias entre los componentes de la aplicación utilizando el patrón de inyección de dependencias.

//Funcionalidad: Los objetos no crean sus dependencias, sino que se las proporciona un contenedor IoC (Inversion of Control). Esto permite un código más limpio y menos acoplado, facilitando su mantenimiento y pruebas.

//Tipos de Inyección: Spring soporta diferentes tipos de inyección, como la inyección por constructor, la inyección por setter y la inyección de campo. Cada tipo tiene sus ventajas y desventajas, y la elección depende del diseño específico de la aplicación.

